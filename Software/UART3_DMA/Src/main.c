void process_frame(int offset)
{
  for (int i = 0; i < 50; i += 2)
  {
    sampleA0 = uart2_dma_buff[offset + i];
    sumA0 += sampleA0;
    sampleA1 = uart2_dma_buff[offset + i + 1];
    sumA1 += sampleA1;
  }
}
